# Integer to String
num = 100
result = str(num)
print(result, type(result))

# Float to String
num = 10.45
result = str(num)
print(result, type(result))

# String to Integer
string = "45"
result = int(string)
print(result, type(result))

# String to Integer
string = "56.83"
