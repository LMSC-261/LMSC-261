# All comparison operations result in Boolean type (True or False)

# Declare integer type variables for comparison
a = 10
b = 20
result = False

# Check if a is equal to b
result = a == b
print("Is", a, "equal to", b, "?\t")
print(result)

# check if a is not equal to b
result = a != b
print("Is", a, "not equal to", b, "?")
print(result)

# check if a is less than b
result = a < b
print("Is", a, "less than", b, "?")
print(result)

# check if a is less than or equal to b
result = a <= b
print("Is", a, "less than or equal to", b, "?")
print(result)

# check if a is greater than b
result = a > b 
print("Is", a, "not greater than", b, "?")
print(result)

# check if a is greater than or equal to b
result = a >= b
print("Is", a, "greater than or equal to", b, "?")
print(result)