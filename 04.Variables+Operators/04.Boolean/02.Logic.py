# All logical operations result in Boolean type (True or False)

# Declare variables a and b to hold boolean values
a = True
b = False

# print the original truth value of a and b
print(a, b)

# invert the truth value of a and b
print(not a, not b)

# operate on a and b using logical 'and'
print(a and b)

# operate on a and b using logical 'or'
print(a or b)
